package com.elijah.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Equal to (==)
        System.out.printf("a == b: %b\n", a == b);
        // Not equal to (!=)
        System.out.printf("a != b: %b\n", a != b);
        // Greater than (>)
        System.out.printf("a > b: %b\n", a > b);
        // Less than (<)
        System.out.printf("a < b: %b\n", a < b);
        // Greater than or equal to (>=)
        System.out.printf("a >= b: %b\n", a >= b);
        // Less than or equal to (<=)
        System.out.printf("a <= b: %b\n", a <= b);

    }
}
